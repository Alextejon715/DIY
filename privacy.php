<p>Podemos recopilar información de identificación personal de los usuarios de varias maneras, incluyendo, pero no limitado a, cuando los usuarios visitan nuestro sitio, suscribirse al boletín, llenar un formulario, y en conexión con otras actividades, servicios, características o recursos que ponemos a disposición en nuestro sitio. Se puede pedir a los usuarios, según corresponda, nombre, dirección de correo electrónico. Sin embargo, los usuarios pueden visitar nuestro Sitio de forma anónima. Recopilaremos información de identificación personal de los usuarios solo si nos envían voluntariamente dicha información. Los usuarios siempre pueden negarse a proporcionar información de identificación personal, excepto que puede impedir que se involucren en determinadas actividades relacionadas con el Sitio.</p>



<p><b>Información de Identificación No Personal</b></p>



<p>Podemos recopilar información de identificación no personal sobre los usuarios siempre que interactúen con nuestro Sitio. La información de identificación no personal puede incluir el nombre del navegador, el tipo de ordenador e información técnica sobre los medios de conexión de los usuarios a nuestro Sitio, como el sistema operativo y los proveedores de servicios de Internet utilizados y otra información similar.</p>



