<p>Nuestro Sitio puede utilizar "cookies" para mejorar la experiencia del Usuario. El navegador web del usuario coloca cookies en su disco duro con fines de mantenimiento de registros y, a veces, para realizar un seguimiento de la información sobre ellos. El usuario puede elegir configurar su navegador web para que rechace las cookies o para que le avise cuando se envíen cookies. Si lo hace, tenga en cuenta que algunas partes del Sitio pueden no funcionar correctamente.</p>



